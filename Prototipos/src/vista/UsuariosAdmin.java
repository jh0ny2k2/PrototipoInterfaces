    private void cargarUsuariosEnTabla() {
        
        jTableUsuarios = new JTable();
        JScrollPane scrollPane = new JScrollPane(jTableUsuarios); 
        this.add(scrollPane);
        
        String filePath = System.getProperty("user.home") + File.separator + "DatosLoginRegistro.txt"; // Ruta al archivo
        File archivo = new File(filePath);

        if (!archivo.exists()) {
            JOptionPane.showMessageDialog(this, "El archivo de usuarios no existe.");
            return;
        }

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Usuario");
        modelo.addColumn("Contrase√±a");

        try (BufferedReader reader = new BufferedReader(new FileReader(archivo))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes.length == 2) {
                    modelo.addRow(new Object[]{partes[0].trim(), partes[1].trim()});
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo: " + e.getMessage());
        }

        jTableUsuarios.setModel(modelo);
    }

